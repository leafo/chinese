#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

const AUDIO_DIR = path.join(__dirname, '..', 'dist', 'collections', 'audio');
const OUTPUT_FILE = path.join(__dirname, '..', 'src', 'audio-manifest.js');

const files = fs.readdirSync(AUDIO_DIR)
  .filter(f => f.endsWith('.mp3'))
  .sort();

const entries = files.map(f => {
  const key = path.basename(f, '.mp3');
  return `  ${JSON.stringify(key)}: ${JSON.stringify(`collections/audio/${f}`)}`;
});

const output = `// Auto-generated by scripts/generate-audio-manifest.js
export default {
${entries.join(',\n')},
};
`;

fs.writeFileSync(OUTPUT_FILE, output);
console.log(`Wrote ${files.length} entries to ${path.relative(process.cwd(), OUTPUT_FILE)}`);
